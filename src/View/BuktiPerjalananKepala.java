    public void save(){
        try {

            if (txtNomor.getText().equals("")&&txtNomor1.getText().equals("") && txtDeskripsi.getText().equals("") ) {
                JOptionPane.showMessageDialog(null, "Data Tidak lengkap");
            } else {
                Statement statement = (Statement) SPPDUtil.getConnection().createStatement();        
                String query = "insert into buktiperjalanan values ('" + txtNomor.getText() + "','" + txtNomor1.getText() + "','" + txtDeskripsi.getText()+"')";
                
                int res = statement.executeUpdate(query);
                if (res == 1) {
                    JOptionPane.showMessageDialog(null, "Insert Data Sukses !");
                    
                }
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        semuadata();
    }
    private void semuadata(){
            DefaultTableModel data = new DefaultTableModel();
            data.addColumn("Nomor Bukti");
            data.addColumn("Nomor SPPD");
            data.addColumn("Deskripsi");           
             try{
                 getConnection();
                String sql = "Select * from buktiperjalanan";
                Statement statement = (Statement) SPPDUtil.getConnection().createStatement();
                ResultSet hasil = statement.executeQuery(sql);
                while(hasil.next()){
                    data.addRow(new Object[]{hasil.getString(1),hasil.getString(2),hasil.getString(3)});
                }
                    tabelBuktiPerjalanan.setModel(data);
                
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "error :"+e.getMessage());
        }
    }

